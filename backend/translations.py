DEFAULT_LANG = "tk"
SUPPORTED_LANGS = ("en", "ru", "tk")


def translate(key, lang):
    """Look up `key` in the given language, falling back to English, then
    to the raw key itself if nothing is found."""
    lang_dict = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG])
    if key in lang_dict:
        return lang_dict[key]
    return TRANSLATIONS["en"].get(key, key)
